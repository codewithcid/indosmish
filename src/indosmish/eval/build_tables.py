"""Aggregate all results/*.json into markdown tables that map onto the paper.

Produces results/RESULTS.md with:
 - a master per-model/per-arm table (macro-F1, accuracy, smishing recall),
 - the FP16-vs-4-bit quantization delta (paper's headline gap G3),
 - a deployment profiling table (G4).

Run:  python -m indosmish.eval.build_tables
"""
import json
from pathlib import Path

from ..config import resolve


def _load_all() -> list[dict]:
    out = []
    rdir = resolve("results")
    for p in rdir.glob("*.json"):
        if p.name in ("profiling.json",):
            continue
        try:
            d = json.loads(p.read_text(encoding="utf-8"))
        except json.JSONDecodeError:
            continue
        if isinstance(d, dict) and "macro_f1" in d:
            d["_file"] = p.stem
            out.append(d)
    return out


def _master_table(rows: list[dict]) -> str:
    lines = ["| Model | Arm | macro-F1 | Accuracy | Smishing recall |",
             "|-------|-----|---------:|---------:|----------------:|"]
    for r in sorted(rows, key=lambda x: (x.get("model", ""), x.get("arm", ""))):
        lines.append(
            f"| {r.get('model', r['_file'])} | {r.get('arm', '-')} | "
            f"{r['macro_f1']:.3f} | {r['accuracy']:.3f} | {r['smishing_recall']:.3f} |"
        )
    return "\n".join(lines)


def _quant_delta(rows: list[dict]) -> str:
    fp16 = {r["arm"]: r for r in rows if "fp16" in r["_file"] and "slm" in r["_file"]}
    q4 = {r["arm"]: r for r in rows if "q4" in r["_file"] and "slm" in r["_file"]}
    if not fp16 or not q4:
        return "_Run the FP16 and 4-bit SLM evals to populate this table._"
    lines = ["| Arm | FP16 macro-F1 | 4-bit macro-F1 | Δ | FP16 smish-recall | 4-bit smish-recall | Δ |",
             "|-----|--------------:|---------------:|---:|------------------:|-------------------:|---:|"]
    for arm in ("roman", "native"):
        if arm in fp16 and arm in q4:
            a, b = fp16[arm], q4[arm]
            lines.append(
                f"| {arm} | {a['macro_f1']:.3f} | {b['macro_f1']:.3f} | "
                f"{b['macro_f1'] - a['macro_f1']:+.3f} | {a['smishing_recall']:.3f} | "
                f"{b['smishing_recall']:.3f} | {b['smishing_recall'] - a['smishing_recall']:+.3f} |"
            )
    return "\n".join(lines)


def _profiling_table() -> str:
    p = resolve("results/profiling.json")
    if not p.exists():
        return "_Run indosmish.eval.profile_device to populate this table._"
    rows = json.loads(p.read_text(encoding="utf-8"))
    lines = ["| Device | Precision | Size (MB) | Peak RSS (MB) | Latency mean (ms) | p95 (ms) |",
             "|--------|-----------|----------:|--------------:|------------------:|---------:|"]
    for r in rows:
        lines.append(
            f"| {r['device']} | {r['precision']} | {r['model_size_mb']} | "
            f"{r['peak_rss_mb']} | {r['latency_ms_mean']} | {r['latency_ms_p95']} |"
        )
    return "\n".join(lines)


def main() -> None:
    rows = _load_all()
    md = f"""# IndoSmish Results

Auto-generated by `indosmish.eval.build_tables`. Maps to the paper's Table 10
(target benchmark row) and Table 12 (gaps G1–G4).

## Master results (per model, per script arm)

{_master_table(rows) if rows else "_No results yet._"}

## Quantization delta (G3 — the paper's headline missing measurement)

FP16 vs 4-bit, per class and per script arm.

{_quant_delta(rows)}

## Deployment profiling (G4 — named hardware)

{_profiling_table()}
"""
    out = resolve("results/RESULTS.md")
    out.parent.mkdir(parents=True, exist_ok=True)
    out.write_text(md, encoding="utf-8")
    print(f"Wrote {out} ({len(rows)} result files aggregated)")


if __name__ == "__main__":
    main()
